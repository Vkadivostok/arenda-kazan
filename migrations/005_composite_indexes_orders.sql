-- Миграция 005: составные индексы под типичные паттерны выборки.
-- Самый частый запрос — фильтрация очереди по статусу + дате (главный экран),
-- одиночные индексы из миграции 001 в этом случае работают хуже составного.
-- Также добавляем индекс по client_name для поиска заказов через searchOrders().

-- Составной индекс под выборку очереди: WHERE status = ? AND queue_date ...
CREATE INDEX idx_orders_status_queue_date ON orders(status, queue_date);

-- Поиск заказов по имени клиента (LIKE 'X%' использует индекс,
-- LIKE '%X%' — нет, но даже здесь индекс даёт covering-эффект для коротких выборок).
CREATE INDEX idx_orders_client_name ON orders(client_name);
